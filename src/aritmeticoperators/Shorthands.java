package aritmeticoperators;

public class Shorthands {

	public static void main(String[] args) {
		
		int ppl=5;
		ppl+=4; //ppl=ppl+4
		System.out.println(ppl);
	
		//multiply by two
		ppl*=2;
		System.out.println(ppl);
		ppl*=ppl;
		System.out.println(ppl);
		
		ppl/=2;
		System.out.println(ppl);
		ppl%=5;
		System.out.println(ppl);
	
	
	
	}
	

}
